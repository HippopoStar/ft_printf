#include "libft.h"

static void	aux_ft_strclr(char *s)
{
	size_t	i;

	i = 0;
	while (*(s + i) != '\0')
	{
		*(s + i) = '\0';
		i++;
	}
}

void		ft_strclr(char *s)
{
	if (s != NULL)
	{
		aux_ft_strclr(s);
	}
}
